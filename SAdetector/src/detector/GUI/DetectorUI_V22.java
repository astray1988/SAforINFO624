    public String removeDuplicates(String s) {
        StringBuilder noDupes = new StringBuilder();
        int pos = 0 ;
        int len = s.length();
        int curLen = 0;
        while (pos < len) {
            if (pos == 0 || s.charAt(pos) != s.charAt(pos - 1)) {
                curLen = 1;
            } else {
                ++curLen;
            }
            if (curLen < 3) {
                noDupes.append(s.charAt(pos));
            }
            ++pos;
        }
        return noDupes.toString();
        }
            
